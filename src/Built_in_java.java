public class Built_in_java {
    public static void main(String[] args) {
        //Built-in math function in java

        System.out.println("Absolute value : "+Math.abs(-45));
        System.out.println("Round value : "+Math.round(2.556));
        System.out.println("Ceil value : "+Math.ceil(2.24));
        System.out.println("Floor value : "+Math.floor(2.34));

        double a=2;
        double b=3;
        System.out.println("Maximum no. "+Math.max(a,b));
        System.out.println("Minimum no. "+Math.min(a,b));
        System.out.println("Square root of a no. "+Math.sqrt(a));
        System.out.println("Power. "+Math.pow(a,b));
        System.out.println("Logarith of a no. "+Math.log(a));
        System.out.println("Log10 of a is "+Math.log10(a));
        System.out.println("Sine value "+Math.sin(a));
        System.out.println("Cosine Value "+Math.cos(a));
        System.out.println("Tangent Value "+Math.tan(a));
    }
}
